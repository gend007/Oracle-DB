--社員マスタ
CREATE TABLE SYAIN_M (
  SYAIN_CD char(6),
  YUUKOU_FROM date,
  YUUKOU_TO date,
  SYAIN_NM varchar2(64),
  SYAIN_NM_KN varchar2(128),
  BUMON_CD char(8),
  SYOKUI_CD char(3),
  PRIMARY KEY (SYAIN_CD,YUUKOU_FROM)
);

COMMENT ON TABLE SYAIN_M IS '社員マスタ';
COMMENT ON COLUMN SYAIN_M.SYAIN_CD IS '社員コード';
COMMENT ON COLUMN SYAIN_M.YUUKOU_FROM IS '有効期間From';
COMMENT ON COLUMN SYAIN_M.YUUKOU_TO IS '有効期間To';
COMMENT ON COLUMN SYAIN_M.SYAIN_NM IS '社員名';
COMMENT ON COLUMN SYAIN_M.SYAIN_NM_KN IS '社員名カナ';
COMMENT ON COLUMN SYAIN_M.BUMON_CD IS '部門コード';
COMMENT ON COLUMN SYAIN_M.SYOKUI_CD IS '職位コード';


